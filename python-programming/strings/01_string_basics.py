# Defining strings
s1 = "Hello"
s2 = 'World'
s3 = """This is
a multiline string"""

print("s1:", s1)
print("s2:", s2)
print("s3:", s3)

# Indexing
print("First character of s1:", s1[0])
print("Last character of s2:", s2[-1])

# Slicing
print("First three chars of s1:", s1[:3])
print("s2 reversed:", s2[::-1])

# Concatenation
greeting = s1 + " " + s2
print("Concatenation:", greeting)

# Repetition
print("Repeat s1 3 times:", s1 * 3)

