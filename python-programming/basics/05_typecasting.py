
x = "10"
y = "3.5"

# Convert string to int
a = int(x)
# Convert string to float
b = float(y)

# Convert number to string
c = str(a + int(float(b)))

print("a (int):", a)
print("b (float):", b)
print("Sum as string:", c)

# Other examples
print("Boolean of 0:", bool(0))       # False
print("Boolean of 5:", bool(5))       # True

