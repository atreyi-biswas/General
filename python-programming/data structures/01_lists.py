# Creating a list
fruits = ["apple", "banana", "cherry"]

# Append
fruits.append("orange")
print("After append:", fruits)

# Pop (removes last element by default)
fruits.pop()
print("After pop:", fruits)

# Pop at index
fruits.pop(1)
print("After popping index 1:", fruits)

# Slicing
print("Slicing [0:2]:", fruits[0:2])
print("Reversed list:", fruits[::-1])

